class SumAve1{
    public static void main(String[] args) {

        int x;
        int y;

        x=63;
        y=18;

        System.out.println("x값은"+x+"입니다.");
        System.out.println("y값은"+y+"입니다.");
        System.out.println("합계는"+(x+y)+"입니다.");
        System.out.println("퍙균은"+(x+y)/2+"입니다.");
        //정수/정수는 소수점을 버리고 계산되서 40.5가 아니라 40이다.
    }
}
